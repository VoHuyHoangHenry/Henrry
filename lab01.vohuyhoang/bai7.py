import math 
r = float (input("Nhập bán kính hình tròn: "))
#Công thúc tính chu vi hình tròn
cv = 2 * math.pi * r
print ("Chu vi hình tròn là: ", cv)
#Công thức tính diện tích hình tròn
dt = math.pi * r**2
print ("Diện tích hình tròn là: ", dt)


