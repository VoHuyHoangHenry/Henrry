a  = int(input("Nhập hệ số a: "))
b  = int(input("Nhập hệ số b: "))
c  = int(input("Nhập hệ số c: "))
#Tìm số lớn nhất và nhỏ nhất
lon_nhat = max(a, b, c)
nho_nhat = min(a, b, c)
print ("Số lớn nhất là: ", lon_nhat)
print("Số nhỏ nhất là: ", nho_nhat)

